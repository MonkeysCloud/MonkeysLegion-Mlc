<?php

declare(strict_types=1);

namespace MonkeysLegion\Mlc\Tests\Unit\Cache;

use MonkeysLegion\Mlc\Cache\CompiledPhpCache;
use MonkeysLegion\Mlc\Config;
use MonkeysLegion\Mlc\Exception\FrozenConfigException;
use PHPUnit\Framework\Attributes\CoversClass;
use PHPUnit\Framework\Attributes\Group;
use PHPUnit\Framework\TestCase;

#[CoversClass(CompiledPhpCache::class)]
#[CoversClass(Config::class)]
final class CompiledPhpCacheTest extends TestCase
{
    private string $cacheDir;
    private CompiledPhpCache $cache;

    protected function setUp(): void
    {
        $this->cacheDir = __DIR__ . '/../../.cache_test';
        if (!is_dir($this->cacheDir)) {
            mkdir($this->cacheDir, 0775, true);
        }
        $this->cache = new CompiledPhpCache($this->cacheDir);
    }

    protected function tearDown(): void
    {
        $this->cache->clear();
        if (is_dir($this->cacheDir)) {
            rmdir($this->cacheDir);
        }
    }

    // =========================================================================
    // CompiledPhpCache — PSR-16 contract
    // =========================================================================

    #[Group('cache')]
    public function test_set_and_get(): void
    {
        $this->assertTrue($this->cache->set('foo', 'bar'));
        $this->assertSame('bar', $this->cache->get('foo'));
    }

    #[Group('cache')]
    public function test_get_non_existent_returns_default(): void
    {
        $this->assertNull($this->cache->get('nothing'));
        $this->assertSame('default', $this->cache->get('nothing', 'default'));
    }

    #[Group('cache')]
    public function test_has(): void
    {
        $this->assertFalse($this->cache->has('foo'));
        $this->cache->set('foo', 'bar');
        $this->assertTrue($this->cache->has('foo'));
    }

    #[Group('cache')]
    public function test_delete(): void
    {
        $this->cache->set('foo', 'bar');
        $this->assertTrue($this->cache->delete('foo'));
        $this->assertNull($this->cache->get('foo'));
        $this->assertFalse($this->cache->has('foo'));
    }

    #[Group('cache')]
    public function test_delete_non_existent_returns_true(): void
    {
        // PSR-16: delete() MUST return true even if the key did not exist
        $this->assertTrue($this->cache->delete('ghost'));
    }

    #[Group('cache')]
    public function test_clear(): void
    {
        $this->cache->set('a', 1);
        $this->cache->set('b', 2);
        $this->assertTrue($this->cache->clear());
        $this->assertNull($this->cache->get('a'));
        $this->assertNull($this->cache->get('b'));
    }

    // =========================================================================
    // CompiledPhpCache — immutability / OPcache contract
    // =========================================================================

    #[Group('cache')]
    public function test_ttl_is_ignored_cache_is_immutable(): void
    {
        // TTL must be silently ignored — CompiledPhpCache is immutable by design.
        $this->cache->set('key', 'value', 1);   // ttl=1s — ignored
        $this->assertSame('value', $this->cache->get('key'));
        $this->assertTrue($this->cache->has('key'));
    }

    #[Group('cache')]
    public function test_compiled_file_is_valid_php(): void
    {
        $data = ['database' => ['host' => 'localhost', 'port' => 3306]];
        $this->cache->set('config', $data);

        $safe = preg_replace('/[^A-Za-z0-9_.\-]/', '_', 'config');
        $path = $this->cacheDir . DIRECTORY_SEPARATOR . $safe . '.generated.php';

        $this->assertFileExists($path);
        $contents = file_get_contents($path);
        $this->assertStringStartsWith('<?php', $contents);
        $this->assertStringContainsString('return ', $contents);
        $this->assertStringContainsString('Generated by MonkeysLegion CompiledPhpCache', $contents);
    }

    #[Group('cache')]
    public function test_compiled_file_is_reloaded_as_array(): void
    {
        $data = ['key' => 'value', 'nested' => ['a' => true, 'b' => 42]];
        $this->cache->set('reload_test', $data);

        $freshCache = new CompiledPhpCache($this->cacheDir);
        $this->assertSame($data, $freshCache->get('reload_test'));
    }

    #[Group('cache')]
    public function test_set_overwrites_existing_key(): void
    {
        $this->cache->set('key', 'first');
        $this->cache->set('key', 'second');
        $this->assertSame('second', $this->cache->get('key'));
    }

    #[Group('cache')]
    public function test_complex_nested_data(): void
    {
        $data = [
            'nested' => ['a' => 1, 'b' => true, 'c' => null],
            'list'   => [1, 2, 3],
            'string' => "Hello\nWorld",
            'float'  => 3.14,
        ];

        $this->cache->set('complex', $data);
        $this->assertSame($data, $this->cache->get('complex'));
    }

    // =========================================================================
    // CompiledPhpCache — PSR-16 multiple helpers
    // =========================================================================

    #[Group('cache')]
    public function test_get_multiple(): void
    {
        $this->cache->set('x', 10);
        $this->cache->set('y', 20);

        $result = iterator_to_array($this->cache->getMultiple(['x', 'y', 'z'], 'missing'));

        $this->assertSame(10, $result['x']);
        $this->assertSame(20, $result['y']);
        $this->assertSame('missing', $result['z']);
    }

    #[Group('cache')]
    public function test_set_multiple(): void
    {
        $this->assertTrue($this->cache->setMultiple(['p' => 'ping', 'q' => 'pong']));
        $this->assertSame('ping', $this->cache->get('p'));
        $this->assertSame('pong', $this->cache->get('q'));
    }

    #[Group('cache')]
    public function test_delete_multiple(): void
    {
        $this->cache->set('m', 1);
        $this->cache->set('n', 2);
        $this->assertTrue($this->cache->deleteMultiple(['m', 'n']));
        $this->assertFalse($this->cache->has('m'));
        $this->assertFalse($this->cache->has('n'));
    }

    // =========================================================================
    // Config — default state: no locks, dual-layer dormant
    // =========================================================================

    #[Group('dual-layer')]
    public function test_fresh_config_has_nothing_locked(): void
    {
        $config = new Config(['key' => 'val']);

        $this->assertFalse($config->isLocked());
        $this->assertFalse($config->areOverridesLocked());
        $this->assertFalse($config->isDualLayerActive());
    }

    #[Group('dual-layer')]
    public function test_get_reads_bypasses_override_layer_when_dormant(): void
    {
        $config = new Config(['db' => ['host' => 'localhost']]);

        // Dual-layer is dormant — direct path, no override-layer overhead
        $this->assertFalse($config->isDualLayerActive());
        $this->assertSame('localhost', $config->get('db.host'));
    }

    // =========================================================================
    // Config — dual-layer auto-activation
    // =========================================================================

    #[Group('dual-layer')]
    public function test_first_override_activates_dual_layer(): void
    {
        $config = new Config(['x' => 1]);
        $this->assertFalse($config->isDualLayerActive());

        $config->override('x', 2);

        $this->assertTrue($config->isDualLayerActive());
        $this->assertSame(2, $config->get('x'));
    }

    #[Group('dual-layer')]
    public function test_override_shadows_base_value(): void
    {
        $config = new Config(['db' => ['host' => 'localhost', 'port' => 3306]]);
        $config->override('db.host', '10.0.0.1');

        $this->assertSame('10.0.0.1', $config->get('db.host'));
        // Base layer for sibling key untouched
        $this->assertSame(3306, $config->get('db.port'));
    }

    #[Group('dual-layer')]
    public function test_override_adds_new_key(): void
    {
        $config = new Config(['existing' => 'yes']);
        $config->override('brand_new', 'runtime_value');

        $this->assertSame('runtime_value', $config->get('brand_new'));
        $this->assertNull($config->get('non_existing'));
    }

    #[Group('dual-layer')]
    public function test_base_layer_is_never_mutated(): void
    {
        $config = new Config(['env' => 'production']);
        $config->override('env', 'testing');

        // all() returns the compiled base — override must NOT appear there
        $this->assertSame('production', $config->all()['env']);

        // get() applies override priority
        $this->assertSame('testing', $config->get('env'));
    }

    #[Group('dual-layer')]
    public function test_multiple_overrides_accumulate(): void
    {
        $config = new Config(['a' => 1, 'b' => 2, 'c' => 3]);
        $config->override('a', 10);
        $config->override('b', 20);

        $this->assertSame(10, $config->get('a'));
        $this->assertSame(20, $config->get('b'));
        $this->assertSame(3,  $config->get('c')); // untouched
    }

    #[Group('dual-layer')]
    public function test_get_overrides_returns_map(): void
    {
        $config = new Config(['x' => 1]);
        $config->override('x', 99);
        $config->override('y', 'new');

        $overrides = $config->getOverrides();
        $this->assertSame(['x' => 99, 'y' => 'new'], $overrides);
    }

    // =========================================================================
    // Config — lock() : Lock 1 (sealed, no overrides allowed at all)
    // =========================================================================

    #[Group('dual-layer')]
    public function test_lock_sets_flag(): void
    {
        $config = new Config(['x' => 1]);
        $this->assertFalse($config->isLocked());
        $config->lock();
        $this->assertTrue($config->isLocked());
    }

    #[Group('dual-layer')]
    public function test_lock_blocks_override(): void
    {
        $config = new Config(['key' => 'val']);
        $config->lock();

        $this->expectException(FrozenConfigException::class);
        $this->expectExceptionMessageMatches('/sealed/');
        $config->override('key', 'new');
    }

    #[Group('dual-layer')]
    public function test_lock_after_some_overrides_preserves_existing(): void
    {
        $config = new Config(['a' => 1]);
        $config->override('a', 99);  // dual-layer activated
        $config->lock();              // sealed now

        // Existing override is still visible
        $this->assertSame(99, $config->get('a'));

        // But no more overrides
        $this->expectException(FrozenConfigException::class);
        $config->override('a', 999);
    }

    #[Group('dual-layer')]
    public function test_lock_does_not_block_get(): void
    {
        $config = new Config(['k' => 'v']);
        $config->lock();

        // Reading is always allowed
        $this->assertSame('v', $config->get('k'));
    }

    #[Group('dual-layer')]
    public function test_lock_does_not_block_snapshot(): void
    {
        $config = new Config(['k' => 'v']);
        $config->lock();

        $snap = $config->snapshot();
        $this->assertSame('v', $snap->get('k'));
    }

    // =========================================================================
    // Config — lockOverrides() : Lock 2 (override layer sealed after some applied)
    // =========================================================================

    #[Group('dual-layer')]
    public function test_lock_overrides_sets_flag(): void
    {
        $config = new Config(['x' => 1]);
        $this->assertFalse($config->areOverridesLocked());
        $config->lockOverrides();
        $this->assertTrue($config->areOverridesLocked());
    }

    #[Group('dual-layer')]
    public function test_lock_overrides_blocks_further_overrides(): void
    {
        $config = new Config(['feature' => false]);
        $config->override('feature', true);
        $config->lockOverrides();

        $this->expectException(FrozenConfigException::class);
        $this->expectExceptionMessageMatches('/override layer is sealed/');
        $config->override('feature', false);
    }

    #[Group('dual-layer')]
    public function test_lock_overrides_preserves_applied_overrides(): void
    {
        $config = new Config(['mode' => 'prod']);
        $config->override('mode', 'debug');
        $config->lockOverrides();

        $this->assertSame('debug', $config->get('mode'));
    }

    #[Group('dual-layer')]
    public function test_lock_overrides_before_any_override_acts_like_lock(): void
    {
        $config = new Config(['x' => 1]);
        $config->lockOverrides(); // no overrides applied yet

        $this->expectException(FrozenConfigException::class);
        $config->override('x', 2);
    }

    // =========================================================================
    // Config — snapshot()
    // =========================================================================

    #[Group('dual-layer')]
    public function test_snapshot_returns_new_instance(): void
    {
        $config = new Config(['a' => 1]);
        $this->assertNotSame($config, $config->snapshot());
    }

    #[Group('dual-layer')]
    public function test_snapshot_with_no_dual_layer_clones_base(): void
    {
        $config = new Config(['k' => 'v']);
        $snap   = $config->snapshot();

        $this->assertSame('v', $snap->get('k'));
        $this->assertFalse($snap->isDualLayerActive());
    }

    #[Group('dual-layer')]
    public function test_snapshot_merges_overrides_into_base(): void
    {
        $config = new Config(['db' => ['host' => 'localhost', 'port' => 5432]]);
        $config->override('db.host', 'db.internal');

        $snap = $config->snapshot();

        $this->assertSame('db.internal', $snap->get('db.host'));
        $this->assertSame(5432,          $snap->get('db.port'));
        // Snapshot starts fresh — dual-layer dormant
        $this->assertFalse($snap->isDualLayerActive());
    }

    #[Group('dual-layer')]
    public function test_snapshot_is_isolated_from_original(): void
    {
        $config = new Config(['counter' => 0]);
        $snap   = $config->snapshot();

        $config->override('counter', 99);

        $this->assertSame(0, $snap->get('counter'));
    }

    #[Group('dual-layer')]
    public function test_snapshot_of_locked_config_is_unlocked(): void
    {
        $config = new Config(['k' => 'v']);
        $config->lock();

        $snap = $config->snapshot();

        // Fresh snapshot — not locked, overrides allowed
        $this->assertFalse($snap->isLocked());
        $snap->override('k', 'mutated');
        $this->assertSame('mutated', $snap->get('k'));
    }

    #[Group('dual-layer')]
    public function test_snapshot_after_lock_overrides_is_unlocked(): void
    {
        $config = new Config(['a' => 1]);
        $config->override('a', 2);
        $config->lockOverrides();

        $snap = $config->snapshot();

        $this->assertFalse($snap->areOverridesLocked());
        $snap->override('a', 3); // must not throw
        $this->assertSame(3, $snap->get('a'));
    }

    // =========================================================================
    // CompiledPhpCache — failure paths
    // =========================================================================

    #[Group('cache')]
    public function test_constructor_throws_on_non_creatable_directory(): void
    {
        if (get_current_user() === 'root') {
            $this->markTestSkipped('Cannot test permission errors as root');
        }

        // Point to a path whose parent is read-only so mkdir() will fail
        $readonlyParent = __DIR__ . '/../../.cache_readonly_parent';
        mkdir($readonlyParent, 0555, true);

        try {
            $this->expectException(\RuntimeException::class);
            $this->expectExceptionMessageMatches('/Directory does not exist/');

            new CompiledPhpCache($readonlyParent . '/cannot_create_me');
        } finally {
            chmod($readonlyParent, 0775);
            rmdir($readonlyParent);
        }
    }

    #[Group('cache')]
    public function test_set_returns_false_when_directory_is_unwritable(): void
    {
        if (get_current_user() === 'root') {
            $this->markTestSkipped('Cannot test permission errors as root');
        }

        // Build cache in a writable dir, then revoke write permission
        $dir = __DIR__ . '/../../.cache_unwritable';
        mkdir($dir, 0775, true);
        $cache = new CompiledPhpCache($dir);

        chmod($dir, 0555); // read + execute only — file_put_contents will fail

        try {
            $result = $cache->set('key', 'value');
            $this->assertFalse($result);
        } finally {
            chmod($dir, 0775);
            // Clean up any temp files that may have been created
            foreach (glob($dir . '/*') ?: [] as $f) {
                @unlink($f);
            }
            rmdir($dir);
        }
    }

    #[Group('cache')]
    public function test_set_multiple_returns_false_when_one_write_fails(): void
    {
        if (get_current_user() === 'root') {
            $this->markTestSkipped('Cannot test permission errors as root');
        }

        $dir = __DIR__ . '/../../.cache_setmulti';
        mkdir($dir, 0775, true);
        $cache = new CompiledPhpCache($dir);

        // Write the first key successfully, then revoke write permission
        $cache->set('ok_key', 'written');
        chmod($dir, 0555);

        try {
            // 'ok_key' already exists and will also fail (rename target unwritable),
            // 'fail_key' has no file — file_put_contents into $dir fails
            $result = $cache->setMultiple(['ok_key' => 'updated', 'fail_key' => 'never']);
            $this->assertFalse($result);
        } finally {
            chmod($dir, 0775);
            foreach (glob($dir . '/*') ?: [] as $f) {
                @unlink($f);
            }
            rmdir($dir);
        }
    }

    #[Group('cache')]
    public function test_delete_multiple_returns_false_when_one_unlink_fails(): void
    {
        if (get_current_user() === 'root') {
            $this->markTestSkipped('Cannot test permission errors as root');
        }

        $dir = __DIR__ . '/../../.cache_delmulti';
        mkdir($dir, 0775, true);
        $cache = new CompiledPhpCache($dir);

        // Write two keys
        $cache->set('alpha', 'a');
        $cache->set('beta', 'b');

        // Revoke write permission on the directory — unlink() needs it
        chmod($dir, 0555);

        try {
            $result = $cache->deleteMultiple(['alpha', 'beta']);
            $this->assertFalse($result);
        } finally {
            chmod($dir, 0775);
            foreach (glob($dir . '/*') ?: [] as $f) {
                @unlink($f);
            }
            rmdir($dir);
        }
    }
}
